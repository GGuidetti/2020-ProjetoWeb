<div class="noticia3">
  <h2> SOFREU ALTERAÇÕES AO CHEGAR NO BRASIL </h2>
  <br>
  <img src="https://www.redebrasilatual.com.br/wp-content/uploads/2020/03/coronavirus-1.jpg" width="350" height="200" alt="">
<h3> <br> O vírus sars-cov-2, causador da covid-19, já se propagou no Brasil a ponto de apresentar características diferentes dos coronavírus que vieram de outros países. É o que aponta um estudo realizado em tempo recorde por cientistas brasileiros, que sequenciaram o genoma da doença em pacientes das regiões Sudeste, Sul e Centro-Oeste. As informações são do jornal O Globo.
Esse trabalho de mapeamento, que se chama vigilância genética viral, é essencial para saber de que forma e quanto a doença se espalha no Brasil. Além disso, comprova geneticamente que há transmissão comunitária no país, reforçando a necessidade do isolamento social para conter o avanço da pandemia.

Durante o estudo, foram mapeados os genomas dos vírus de 19 pessoas, internadas em hospitais de cinco Estados — Minas Gerais, Rio de Janeiro, Goiás, Rio Grande do Sul e São Paulo. Dois dos 19 vírus são de origem asiática, enquanto os outros têm origem europeia. Os cientistas pretendem dar seguimento ao trabalho, a fim de procurar por mutações que possam ser relacionadas à gravidade e à facilidade de transmissão.
Ana Tereza Vasconcelos, uma das autoras do estudo e coordenadora do Laboratório de Bioinformática do Laboratório Nacional de Computação Científica (LNCC), em Petrópolis, afirmou ao jornal que existe entre eles um “cluster” — conjunto de marcas no genoma que aponta que o vírus sofreu alterações após chegar ao país. Segundo ela, as mudanças só ocorrem e são percebidas quando o micro-organismo está há tempo e em quantidade suficientes no local.
— Ele está realmente entre nós — disse Ana ao jornal.
O mapeamento foi resultado de uma força-tarefa que passou um final de semana inteiro trabalhando, durante 48 horas interruptas. A equipe reúne, além do LNCC, profissionais da Universidade Federal do Rio de Janeiro (UFRJ) e da Universidade Federal de Minas Gerais (UFMG), apoiados pelas fundações de amparo a pesquisa dos seus estados (Faperj e Fapemig) e pelo Ministério da Ciência, Tecnologia e Comunicações (MCTIC).
A iniciativa ainda contou com a parceria de pesquisadores do grupo de Ester Sabino, diretora do Instituto de Medicina Tropical da Universidade de São Paulo, e da Universidade de Oxford, na Inglaterra. Os cientistas são da mesma equipe que mapeou, também em tempo recorde, o genoma do vírus que infectou o primeiro paciente no Brasil.
A conclusão do novo estudo em tão pouco tempo só foi possível porque contou com o trabalho voluntário de alunos de pós-graduação de laboratórios que sofreram cortes recentes de bolsas da Coordenação de Aperfeiçoamento de Pessoal de Nível Superior (Capes), vinculada ao Ministério da Educação. O trabalho tem importância na prevenção, contenção e no futuro desenvolvimento de tratamentos da doença.</h3>
