          <h3>  O Ministério da Saúde orienta cuidados básicos para reduzir o risco geral de contrair ou transmitir infecções respiratórias agudas, incluindo o coronavírus. Entre as medidas estão:</h3><br>


<ul class="a">
<li><h2>Lavar as mãos frequentemente com água e sabonete por pelo menos 20 segundos, respeitando os 5 momentos de higienização. Se não houver água e sabonete, usar um desinfetante para as mãos à base de álcool.</h2></li>
<li><h2>Evitar tocar nos olhos, nariz e boca com as mãos não lavadas</h2></li>
<li><h2>Evitar contato próximo com pessoas doentes</h2></li>
<li><h2>Ficar em casa quando estiver doente</h2></li>
<li><h2>Cobrir boca e nariz ao tossir ou espirrar com um lenço de papel e jogar no lixo.</h2></li>
<li><h2>Limpar e desinfetar objetos e superfícies tocados com freqüência.</h2></li>
